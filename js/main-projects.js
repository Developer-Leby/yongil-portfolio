function loadMainProejcts() {    
    let html = '';

    for (let project of mainProjects) {            
        let skillsHtml = '';
        for (let skill of project.skill) {
            skillsHtml += ' ';
            if (skill === 'javascript') skillsHtml += JAVASCRIPT;
            else if (skill === 'vue') skillsHtml += VUE;
            else if (skill === 'angular') skillsHtml += ANGULAR;
            else if (skill === 'spring boot') skillsHtml += SPRING_BOOT;
            else if (skill === 'jquery') skillsHtml += JQUERY;
            else if (skill === 'objective-c') skillsHtml += OBJECTIVE_C;
            else if (skill === 'gulp') skillsHtml += GULP;
        }

        const template = `
            <div class="col-md-4 mb-4 d-flex">
                <div class="blog-entry justify-content-end" style="flex: 0 0 100%;">
                    <img class="block-20 border align-items-stretch" src="${project.thumbnail}" style="width: 100%; height: 200px; object-fit: cover;" />
                    <div class="text mt-3 float-right d-block">                        
                        <span class="text-dark font-weight-bold mb-0" style="width: 100%; font-size: 14px;" title="${project.title}">
                            ${project.title}
                        </span>
                        <p class="mb-0">
                            <span class="text-primary">${project.company}</span>
                        </p>
                        <p class="mb-0">
                            <span class="mr-2">${project.date}</span>
                        </p>
                        <p class="mb-0">
                            ${skillsHtml}
                        </p>
                    </div>
                </div>
            </div>
        `
        html += template
    }
    document.getElementById('main-projects').innerHTML = html;
}

$(function() {
    loadMainProejcts();

    window.addEventListener('resize', () => {
        loadMainProejcts();
    })
});